package com.yulki.operator;

public class OperatorDemo1 {
    public static void main(String[] args) {
        operator(10,3);
    }

    public static void operator(int a,int b){
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}

